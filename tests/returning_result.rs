use lexa_wildcard_matching::WildcardMatching;

#[test]
fn test_returning_result() {
	assert_eq!(
		"PhiSyX[bot]".wildcard_match("PhiSyX[???]"),
		Some("PhiSyX[bot]")
	);
	assert_eq!(
		"PhiSyX[xxx]".wildcard_match("PhiSyX[*]"),
		Some("PhiSyX[xxx]")
	);

	assert_eq!(
		"Hello   World".wildcard_match("Hello*World"),
		Some("Hello   World")
	);
	assert_eq!("Hello   World".wildcard_match("Hello?World"), None);
}
