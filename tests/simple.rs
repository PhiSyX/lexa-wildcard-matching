use lexa_wildcard_matching::WildcardMatching;

#[test]
fn test_iswm_simple() {
	assert!("test".iswm("test"));
	assert!("TEST".iswm("test"));
	assert!(!"test".iswm("failed"));
	assert!("test".iswm("tESt"));

	assert!("TEST".iswmcs("TEST"));
	assert!(!"TEST".iswmcs("test"));
	assert!("tESt".iswmcs("tESt"));
	assert!(!"TEST".iswmcs("tESt"));
}
